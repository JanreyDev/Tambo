// Deep string type -- allows different string values across languages
type DeepString<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
      ? string[]
      : T[K] extends Record<string, unknown>
        ? DeepString<T[K]>
        : T[K];
};

